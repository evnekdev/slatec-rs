/// Returns the reviewed special-function subfamily for a routine.
pub fn group(routine: &str) -> Option<&'static str> {
    if ELEMENTARY.contains(&routine) {
        Some("elementary")
    } else if GAMMA.contains(&routine) {
        Some("gamma")
    } else if BETA.contains(&routine) {
        Some("beta")
    } else if ERROR.contains(&routine) {
        Some("error")
    } else {
        None
    }
}

/// Returns the canonical special-function module for a reviewed routine.
pub fn module(routine: &str) -> Option<&'static str> {
    group(routine).map(|group| match group {
        "elementary" => "special::elementary",
        "gamma" => "special::gamma",
        "beta" => "special::beta",
        "error" => "special::error",
        _ => unreachable!("known foundational group"),
    })
}

/// Returns the public and provider feature for a reviewed routine.
pub fn feature(routine: &str) -> Option<String> {
    group(routine).map(|group| format!("special-{group}"))
}

/// Source-hash guard input for the R2B foundation policy.
pub fn source_manifest(records: &[Value]) -> String {
    let mut entries = records
        .iter()
        .filter(|record| group(&field(record, "routine")).is_some())
        .filter(|record| field(record, "historical_role") == "user_callable")
        .filter(|record| field(record, "generated_declaration_status") == "generated_abi_validated")
        .map(|record| {
            format!(
                "{}\0{}\0{}\n",
                field(record, "routine"),
                field(record, "source_hash"),
                field(record, "canonical_provider"),
            )
        })
        .collect::<Vec<_>>();
    entries.sort();
    hash::bytes(entries.concat().as_bytes())
}

/// Renders the canonical reviewed `slatec_sys::special` module.
pub fn render_module(records: &[Value]) -> String {
    let mut output = String::from(
        "//! Canonical reviewed raw scalar special-function declarations.\n//!\n//! This file is generated by `slatec-corpus generate-raw-api-inventory --offline`.\n//! Each promoted special subfamily has its own source-hash-guarded review policy\n//! and re-exports one private authoritative declaration.\n\n",
    );
    for (group_name, routines) in [
        ("elementary", ELEMENTARY),
        ("gamma", GAMMA),
        ("beta", BETA),
        ("error", ERROR),
    ] {
        let cfg = if group_name == "elementary" {
            format!(
                "#[cfg(any(\n    feature = \"special-{group_name}\",\n    feature = \"raw-family-special-{group_name}\"\n))]"
            )
        } else {
            format!(
                "#[cfg(any(feature = \"special-{group_name}\", feature = \"raw-family-special-{group_name}\"))]"
            )
        };
        output.push_str(&format!(
            "/// Reviewed scalar special-function routines in the `{group_name}` group.\n{cfg}\npub mod {group_name} {{\n"
        ));
        let mut items = records
            .iter()
            .filter(|record| group(&field(record, "routine")) == Some(group_name))
            .filter(|record| {
                field(record, "reviewed_declaration_status") == "reviewed_public_driver"
            })
            .collect::<Vec<_>>();
        items.sort_by_key(|record| field(record, "routine"));
        assert_eq!(
            items.len(),
            routines.len(),
            "reviewed special-foundation policy did not render its complete group"
        );
        for record in items {
            render_routine(&mut output, record);
        }
        if output.ends_with("\n\n") {
            output.pop();
        }
        output.push_str("}\n\n");
    }
    if output.ends_with("\n\n") {
        output.pop();
    }
    output.push('\n');
    output.push_str(&airy_api::render_module(records));
    output
}

/// Creates the machine-readable R2B candidate and disposition report.
pub fn family_report(records: &[Value]) -> Value {
    let mut rows = records
        .iter()
        .filter(|record| group(&field(record, "routine")).is_some())
        .map(|record| {
            let routine = field(record, "routine");
            let spec = spec(&routine).expect("foundational routine has metadata");
            let reviewed = field(record, "reviewed_declaration_status") == "reviewed_public_driver";
            let disposition = if reviewed {
                "reviewed public driver"
            } else if field(record, "generated_declaration_status") != "generated_abi_validated" {
                "missing or ambiguous symbol"
            } else {
                "deferred pending semantic review"
            };
            json!({
                "routine":routine,
                "target_group":group(&routine),
                "purpose":spec.purpose,
                "historical_role":field(record,"historical_role"),
                "precision":spec.precision,
                "return_kind":format!("{} Fortran scalar function", spec.precision),
                "argument_abi_category":"real or INTEGER scalar values passed by address",
                "argument_order":record.get("argument_order").cloned().unwrap_or_else(|| json!([])),
                "canonical_provider":field(record,"canonical_provider"),
                "source_hash":field(record,"source_hash"),
                "source_file":field(record,"source_file"),
                "native_symbol":field(record,"native_symbol"),
                "generated_declaration_status":field(record,"generated_declaration_status"),
                "existing_family_declaration_status":field(record,"legacy_family_declaration_status"),
                "existing_safe_wrapper_audit":field(record,"safe_wrapper_status"),
                "intended_canonical_path":format!("slatec_sys::{}::{}", module(&routine).expect("group"), routine.to_ascii_lowercase()),
                "all_feature_reachability":field(record,"all_feature_reachability"),
                "provider_feature_status":field(record,"provider_feature"),
                "documentation_evidence":record.get("documentation_evidence").cloned().unwrap_or_else(|| json!({})),
                "runtime_global_state_dependencies":spec.state,
                "xerror_fnlib_dependency":spec.error,
                "final_feasibility_disposition":disposition,
                "exclusion_or_deferment_reason":if reviewed { "none".to_owned() } else { field(record,"exclusion_reason") },
            })
        })
        .collect::<Vec<_>>();
    rows.sort_by(|left, right| left["routine"].as_str().cmp(&right["routine"].as_str()));
    let counts = rows
        .iter()
        .fold(BTreeMap::<String, usize>::new(), |mut counts, row| {
            *counts
                .entry(field(row, "final_feasibility_disposition"))
                .or_default() += 1;
            counts
        });
    json!({
        "schema_id":"slatec-sys.raw-api.special-foundations-report",
        "schema_version":"1.0.0",
        "scope":"Every R2B foundational scalar special-function identity selected by the authored elementary, gamma, beta, and error taxonomy.",
        "candidate_count":rows.len(),
        "disposition_counts":counts,
        "records":rows,
    })
}

fn render_routine(output: &mut String, record: &Value) {
    let routine = field(record, "routine");
    let spec = spec(&routine).expect("foundational routine has metadata");
    let group = group(&routine).expect("foundational routine has group");
    let source_file = field(record, "source_file");
    let source_link = format!("https://www.netlib.org/slatec/{source_file}");
    output.push_str(&format!("    // raw-api-routine: {routine}\n"));
    doc_line(output, &format!("Original SLATEC routine `{routine}`."));
    doc_line(output, spec.purpose);
    doc_line(
        output,
        &format!("Mathematical operation: {}.", spec.operation),
    );
    doc_line(
        output,
        &format!(
            "Precision and return ABI: `{}` Fortran scalar function returned directly by the supported GNU MinGW ABI.",
            spec.precision
        ),
    );
    doc_line(
        output,
        &format!("Domain and exceptional behavior: {}", spec.domain),
    );
    doc_line(
        output,
        &format!(
            "Selected provider: `{}`; source: <{source_link}>; native symbol: `{}`.",
            field(record, "canonical_provider"),
            field(record, "native_symbol")
        ),
    );
    doc_line(
        output,
        "Supported ABI profile: `ffi-profile-gnu-mingw-x86_64`; every Fortran scalar input is passed by address.",
    );
    doc_line(output, "");
    doc_line(output, "# Arguments");
    for argument in strings(record.get("argument_order")) {
        doc_line(
            output,
            &format!("- `{argument}`: {}", argument_doc(&routine, &argument)),
        );
    }
    doc_line(output, "");
    doc_line(output, "# Native behavior");
    doc_line(output, spec.error);
    doc_line(output, spec.state);
    doc_line(output, "");
    doc_line(output, "# Safety");
    doc_line(
        output,
        "Every argument pointer must be non-null, correctly aligned, and readable as its documented scalar type for the complete call; no pointer is retained and these functions have no output pointer. The caller must link a provider built for the documented GNU MinGW ABI and must not allow a panic or other unwinding to cross this FFI boundary. Stay inside the documented native domain and avoid inputs that can trigger fatal legacy XERROR behavior. FNLIB initialization and legacy error controls are process-global; this raw declaration does not serialize calls, so callers must provide required process-wide synchronization.",
    );
    output.push_str(&format!(
        "    #[doc(inline)]\n    pub use crate::families::special_{group}::{};\n\n",
        routine.to_ascii_lowercase()
    ));
}

fn doc_line(output: &mut String, text: &str) {
    if text.is_empty() {
        output.push_str("    ///\n");
    } else {
        output.push_str("    /// ");
        output.push_str(text);
        output.push('\n');
    }
}

fn argument_doc(routine: &str, argument: &str) -> &'static str {
    match argument {
        "X" => {
            "input real scalar, passed by address; null is not permitted, the value is read only, and it must satisfy the routine's documented domain."
        }
        "A" if matches!(
            routine,
            "DGAMI" | "DGAMIC" | "DGAMIT" | "GAMI" | "GAMIC" | "GAMIT"
        ) =>
        {
            "input real shape parameter, passed by address; null is not permitted, the value is read only, and the reviewed direct-call domain requires `A > 0`."
        }
        "A" => {
            "first input real parameter, passed by address; null is not permitted, the value is read only, and the documented beta-function domain requires it to be positive."
        }
        "B" => {
            "second input real parameter, passed by address; null is not permitted, the value is read only, and the documented beta-function domain requires it to be positive."
        }
        "PIN" => {
            "first positive beta-distribution shape parameter, passed by address; null is not permitted and the value is read only."
        }
        "QIN" => {
            "second positive beta-distribution shape parameter, passed by address; null is not permitted and the value is read only."
        }
        "N" => {
            "input `FortranInteger`, passed by address; null is not permitted, the value is read only, and it must be non-negative and within the routine's finite-result range."
        }
        "M" => {
            "input `FortranInteger`, passed by address; null is not permitted, the value is read only, and it must satisfy `0 <= M <= N`."
        }
        _ => {
            "input scalar, passed by address; null is not permitted and the value is read only for the complete call."
        }
    }
}

