//! Reviewed raw special-function foundation policy and rendering.
//!
//! R2B deliberately promotes the four scalar FNLIB groups already selected by
//! the public feature graph.  The catalogue's coarse precision classifier is
//! not the source of truth here: each declaration is re-exported from the
//! compiler-observed generated family module and its scalar return ABI is
//! recorded by this policy.

