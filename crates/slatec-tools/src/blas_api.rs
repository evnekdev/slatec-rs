fn vector_doc(routine: &str, _argument: &str, operand: &str) -> String {
    let mutability = if routine.contains("SWAP") || routine.contains("ROT") {
        "in/out"
    } else if operand == "Y"
        && (routine.contains("AXPY")
            || routine.contains("COPY")
            || routine.contains("GEMV")
            || routine.contains("SYMV")
            || routine.contains("HEMV"))
    {
        "output or in/out"
    } else if operand == "X"
        && (routine.ends_with("SCAL")
            || routine.ends_with("SSCAL")
            || routine.ends_with("TRMV")
            || routine.ends_with("TRSV")
            || routine.ends_with("TBMV")
            || routine.ends_with("TBSV")
            || routine.ends_with("TPMV")
            || routine.ends_with("TPSV"))
    {
        "in/out"
    } else {
        "input"
    };
    format!(
        "{mutability} vector operand. For `N > 0`, make the complete source-defined strided extent accessible from the selected Fortran starting position; for a positive increment this is `1 + (N - 1) * INC{}` elements. The native routine retains no pointer.",
        if operand == "X" { "X" } else { "Y" }
    )
}

fn matrix_doc(routine: &str, operand: &str) -> String {
    let inout = (operand == "C"
        && (routine.ends_with("GEMM")
            || routine.ends_with("SYRK")
            || routine.ends_with("SYR2K")
            || routine.ends_with("HERK")
            || routine.ends_with("HER2K")))
        || (operand == "A"
            && (routine.ends_with("GER")
                || routine.ends_with("GERC")
                || routine.ends_with("GERU")
                || routine.ends_with("SYR")
                || routine.ends_with("SYR2")
                || routine.ends_with("HER")
                || routine.ends_with("HER2")))
        || (operand == "B" && (routine.ends_with("TRMM") || routine.ends_with("TRSM")));
    if inout {
        "In/out column-major matrix operand. Its leading dimension and accessible extent must satisfy the selected source for the active flags.".to_owned()
    } else {
        "Input column-major matrix operand. Its leading dimension and accessible extent must satisfy the selected source for the active flags.".to_owned()
    }
}

fn source_link(source_file: &str) -> String {
    if let Some(path) = source_file.strip_prefix("main-src/") {
        format!("https://www.netlib.org/slatec/{path}")
    } else {
        format!("https://www.netlib.org/slatec/{source_file}")
    }
}

fn strings(value: Option<&Value>) -> Vec<String> {
    value
        .and_then(Value::as_array)
        .map(|items| {
            items
                .iter()
                .filter_map(Value::as_str)
                .map(str::to_owned)
                .collect()
        })
        .unwrap_or_default()
}

fn field(value: &Value, key: &str) -> String {
    value
        .get(key)
        .and_then(Value::as_str)
        .unwrap_or_default()
        .to_owned()
}

